    public String _get_module_code(){
        int i=0;
        String codigomodulo;
        if(jComboBox_module_selection.getSelectedItem().toString().contains("-")){
            while(jComboBox_module_selection.getSelectedItem().toString().charAt(i) != '-'){
                i++;
            }
            codigomodulo = jComboBox_module_selection.getSelectedItem().toString().substring(0, i);
        }else{
            codigomodulo = jComboBox_module_selection.getSelectedItem().toString();
        }
        return(codigomodulo);
    }

    private void _addCombobox(){
        try{
            MySQL database = new MySQL();
            Statement st = database.getConect().createStatement();
            
            String query_SQL = ("SELECT * FROM `formasete`.`scannerpecas` WHERE codigopeca = '" + part_code + "' and unidades != unidadeslidas;");
            st.executeQuery(query_SQL);
            ResultSet linhaSQL = st.getResultSet();
            
            while(linhaSQL.next()){
                if(!(linhaSQL.getString("descricaomodulo").equals(""))){
                    jComboBox_module_selection.addItem(linhaSQL.getString("codigomodulo") + " - " + linhaSQL.getString("descricaomodulo"));
                }else{
                    jComboBox_module_selection.addItem(linhaSQL.getString("codigomodulo"));
                }
            }
            
        database.getConect().close();
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
