    private void jLabel_button_confirm_selectionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_button_confirm_selectionMouseClicked
        try{
            MySQL database = new MySQL();
            Statement st = database.getConect().createStatement();
            
            String query_SQL = ("SELECT * FROM `formasete`.`scannerpecas` WHERE (`codigopeca` = '" + part_code + "' and `codigomodulo` = '" + _get_module_code()+ "');");
            st.executeQuery(query_SQL);
            ResultSet linhaSQL = st.getResultSet();
            linhaSQL.first();
            int unidadeslidas = linhaSQL.getInt("unidadeslidas");
            unidadeslidas = unidadeslidas + 1;
            
            query_SQL = ("UPDATE `formasete`.`scannerpecas` SET `unidadeslidas` = '" + unidadeslidas + "' WHERE (`codigopeca` = '" + part_code + "' and `codigomodulo` = '" + _get_module_code()+ "');");
            st.executeUpdate(query_SQL);
            
            database.getConect().close();
            
            Frame_Scanning_Monitor window = new Frame_Scanning_Monitor();
            window.setLocationRelativeTo(null);
            window.setVisible(true);
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        
        JOptionPane.showMessageDialog(null, "Peça escaneada com sucesso!");
        
        this.dispose();
    }//GEN-LAST:event_jLabel_button_confirm_selectionMouseClicked

