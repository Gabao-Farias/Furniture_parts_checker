package model;

public class Parts {
    private String descricaopeca;
    private String codigopeca;
    private int unidades;
    private int unidades_lidas;

    public String getDescricaopeca() {
        return descricaopeca;
    }

    public void setDescricaopeca(String descricaopeca) {
        this.descricaopeca = descricaopeca;
    }

    public String getCodigopeca() {
        return codigopeca;
    }

    public void setCodigopeca(String codigopeca) {
        this.codigopeca = codigopeca;
    }

    public int getUnidades() {
        return unidades;
    }

    public void setUnidades(int unidades) {
        this.unidades = unidades;
    }

    public int getUnidades_lidas() {
        return unidades_lidas;
    }

    public void setUnidades_lidas(int unidades_lidas) {
        this.unidades_lidas = unidades_lidas;
    }
}
